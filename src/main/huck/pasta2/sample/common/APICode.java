package huck.pasta2.sample.common;

public enum APICode {
	SUCCESS(100, "OK"),
	
	// account: 2xx
	AUTH__EMAIL_ALREADY_EXIST(201, "AUTH__EMAIL_ALREADY_EXIST"),
	AUTH__INVALID_SIGNUP_CODE(202, "AUTH__INVALID_SIGNUP_CODE"),
	AUTH__USERID_ALREADY_EXIST(203, "AUTH__USERID_ALREADY_EXIST"),
	AUTH__SIGNIN_FAILED(204, "AUTH__SIGNIN_FAILED"),
	AUTH__UNKNOWN_USERID(205, "AUTH__UNKNOWN_USERID"),

	// product: 3xx
	PRODUCT__UNKNOWN_PRODUCT(301, "PRODUCT__UNKNOWN_PRODUCT"),
	PRODUCT__FORBIDDEN(302, "PRODUCT__FORBIDDEN"),
	
	// product: 4xx
	TRANSACTION__UNKNOWN_TRANSACTION(401, "TRANSACTION__UNKNOWN_TRANSACTION"),
	TRANSACTION__UNAVAILABLE_ACTION(402, "TRANSACTION__UNAVAILABLE_ACTION"),
	
	// common: 9xx	
	COMMON__INTERNAL_ERROR(900, "COMMON__INTERNAL_ERROR"),
	COMMON__PARAMETER_REQUIRED(901, "COMMON__PARAMETER_REQUIRED"),
	COMMON__PARAMETER_INVALID(902, "COMMON__PARAMETER_INVALID"),
	COMMON__EXTERNAL_API_ERROR(903, "COMMON__EXTERNAL_API_ERROR"),
	COMMON__FORM_EXCEPTION(904, "COMMON__FORM_EXCEPTION"),
	
	COMMON__SIGNIN_REQUIRED(910, "COMMON__SIGNIN_REQUIRED"),
	
	COMMON__MAINTENANCE(999, "COMMON__MAINTENANCE"),
	;

	private int code;
	private String codeMsg;
	private APICode(int code, String codeMsg) {
		this.code = code;
		this.codeMsg = codeMsg;
	}
	public int getCode() {
		return code;
	}
	public String getCodeMsg() {
		return codeMsg;
	}
}